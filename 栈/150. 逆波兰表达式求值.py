def evalRPN(tokens):
    stack = []
    aph = ['+', '-', '*', '/']
    for i in tokens:
        if i in aph:
            num2 = stack.pop()
            num1 = stack.pop()
            if i == "+":
                stack.append(num1 + num2)
            elif i == "-":
                stack.append(num1 - num2)
            elif i == "*":
                stack.append(num1 * num2)
            else:
                if num2 < 0 and -num2 > num1:
                    stack.append(0)
                else:
                    stack.append(num1 // num2)
        else:
            stack.append(int(i))
    return stack.pop()


